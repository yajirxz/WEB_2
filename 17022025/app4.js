let numero1 = prompt("ingresa un numero1: ");
let numero2 = prompt("ingresa un numero2: ");
//typeof indica el tipo de dato que se esta ingresando
console.log(typeof numero1);

let resultadoSum = parseInt(numero1) + parseInt(numero2);
console.log("El resultado de la suma : " + resultadoSum);

let resultadoRes = parseInt(numero1) - parseInt(numero2);
console.log("El resultado de la resta : " + resultadoRes);

let resultadoMul = parseInt(numero1) * parseInt(numero2);
console.log("El resultado de la multiplicacion : " + resultadoMul);

let resultadoDiv = parseInt(numero1) / parseInt(numero2);
console.log("El resultado de la division : " + resultadoDiv);